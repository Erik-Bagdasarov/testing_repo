/*
 Скрипт создает 5 таблиц по сцеме
 1. customres
 2. products
 3. shippers
 4. shippinh_details
 5. sales_transactions

 Все таблицы связаны между собой по внешним ключам. Таблицы созданы по схеме "Снежинка"

 В таблице customers храняться данные о клиетам компанни 
 В таблице products храняться данные о продуктах компании
 В таблице shippers является храняться данные о компаниях которые усуществляют доставку товаров
 В таблице shipping_detail храняться детальные данные о доставка товара клиенту
 В таблице sales_transactions храняться данные продажах
 */

drop table if exists sales_transactions;
drop table if exists customers;
drop table if exists products;
drop table if exists shipping_details;
drop table if exists shippers;

create table customers (
    customer_id smallint primary key NOT NULL,
    customer_name character varying(30),
    email character varying(60),
    city character varying(15),
    country character varying(15),
    phone character varying(24)
);

create table products (
    product_id smallint primary key NOT NULL,
    product_name character varying(40) NOT NULL,
    category_id smallint,
    unit_price real,
    units_in_stock smallint
);

create table shippers (
    shipper_id smallint primary key NOT NULL,
    company_name character varying(40) NOT NULL,
    phone character varying(24)
);

create table shipping_details (
    shipping_id smallint primary key NOT NULL,
    shipper_id smallint NOT NULL,
    shipped_date date,
    ship_country character varying(15),
    ship_city character varying(15),
    ship_address character varying(60),
    constraint fk_shipper
        foreign key (shipper_id) references shippers(shipper_id)
);

create table sales_transactions (
    transaction_id smallint primary key NOT NULL,
    customer_id smallint,
    product_id smallint,
    transaction_date date,
    shipping_id smallint,
    quantity int,
    constraint fk_customer
        foreign key (customer_id) references customers(customer_id),
    constraint fk_product
        foreign key (product_id) references products(product_id),
    constraint fk_shipper
        foreign key (shipping_id) references shipping_details(shipping_id)
);

